package Task;

//even number using for loop2
//odd number using for loop

public class Lab006 {
    public static void main(String[] args) {
        System.out.println("Even Numbers");
        for (int i=2;i<=50;i++) {
             System.out.println(i++);
        }
        System.out.println("------------------------");
        System.out.println("Odd Numbers");
        for (int i=1;i<=50;i++) {
            System.out.println(i++);
        }
    }
}
